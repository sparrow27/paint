     private void inicializaBuffer(){
     
     //Creo una imagen del mismo alto y ancho que el lienzo
     
     buffer = (BufferedImage)lienzo.createImage(lienzo.getWidth(), lienzo.getHeight());
     Graphics2D g2 = buffer.createGraphics();
     //Dibujamos un rectangulo blanco del tama√±o del lienzo
     g2.setColor(Color.white);
     g2.fillRect(0,0, lienzo.getWidth(), lienzo.getHeight());
     
     }
    
    

    @Override
    public void paint(Graphics g){
        super.paint (g);
  
          //apunta al lienzo
    Graphics2D g2 = (Graphics2D) lienzo.getGraphics();
     // buffer es el objeto a dibujar, el segundo y el tercer parametros son las coordenadas
     g2.drawImage(buffer, 0, 0, null);
        
    }
    
    
    
    
    
    
    
    
